package iniciado;

public class Caracteres {
    public static void main(String[] args) {
        char letra1 = 'C';
        char letra2 = 'a';
        char letra3 = 'r';
        char letra4 = 'l';
        char letra5 = 'o';
        char letra6 = 's';

        System.out.print(letra1);
        System.out.print(letra2);
        System.out.print(letra3);
        System.out.print(letra4);
        System.out.print(letra5);
        System.out.print(letra6);

        /*
        Usar comillas simples para los caracteres permite al compilador de Java distinguir
        entre caracteres individuales y cadenas de caracteres.
         */
    }
}
